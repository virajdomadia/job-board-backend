import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    role: {
      type: String,
      default: "seeker",
      enum: ["seeker", "employer"],
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("User", userSchema);
